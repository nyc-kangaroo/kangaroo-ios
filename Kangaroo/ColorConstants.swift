import UIKit

extension UIColor {
    
    class func mainKangarooColor() -> UIColor {
        return UIColor(red: 234/255, green: 147/255, blue: 47/255, alpha: 1)
    }
    
    class func darkKangarooColor() -> UIColor {
        return UIColor(red: 27/255, green: 28/255, blue: 30/255, alpha: 1)
    }
}
